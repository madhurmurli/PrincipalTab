package in.principaltab.sqlite;

public class HWMapping {
	private int sync;
	private int homeworkIdLocal;
	private int homeworkId;
	private int opened;
	
	public int getSync() {
		return sync;
	}
	public void setSync(int sync) {
		this.sync = sync;
	}
	public int getHomeworkIdLocal() {
		return homeworkIdLocal;
	}
	public void setHomeworkIdLocal(int homeworkIdLocal) {
		this.homeworkIdLocal = homeworkIdLocal;
	}
	public int getHomeworkId() {
		return homeworkId;
	}
	public void setHomeworkId(int homeworkId) {
		this.homeworkId = homeworkId;
	}
	public int getOpened() {
		return opened;
	}
	public void setOpened(int opened) {
		this.opened = opened;
	}

}
